package com.example.financial_products_management.domain.shared;

/**
 * Enumeración que representa las clasificaciones comerciales para cuentas de ahorro programado para vivienda.
 *
 * @author 200582 Alexander Sandoval
 * @since 2025-09-20
 */
public enum HousingClassification {
    /**
     * Vivienda de Interés Social
     */
    VIS,
    
    /**
     * Vivienda de Interés Propietario
     */
    VIP,
    
    /**
     * Viviendas de Interés Social de Renovación
     */
    VIS_RENOVACION,
    
    /**
     * No de Interés Social
     */
    NO_VIS
}
